def load_evol_instruct(cache_dir: str) -> list[dict]:
    """
    WizardLM/WizardLM_evol_instruct_V2_196k
    Complex, evolved instructions – single-turn.
    """
    ds = load_dataset(
        #"sft_data/hf_cache/WizardLM___wizard_lm_evol_instruct_v2_196k",
        "WizardLM/WizardLM_evol_instruct_V2_196k",
        split="train",
        cache_dir=cache_dir,
        
    )
    out = []
    set_instructions = f"You are a helpful AI Assistant. Below is an instruction that describes a task. Write a detailed, accurate and concise response that appropriately completes the request"
    #f"You are a helpful AI assistant who follows the given instructions and provides a detailed, accurate and concise answers."
    for ex in ds:
        # Dataset has 'conversations' list with human/gpt turns
        conversations = ex.get("conversations", [])
        if len(conversations) < 2:
            continue
        human_turn = conversations[0]
        gpt_turn   = conversations[1]
        if human_turn.get("from", "").lower() not in ("human", "user"):
            continue
        if gpt_turn.get("from", "").lower() not in ("gpt", "assistant"):
            continue
        instruction = human_turn.get("value", "").strip()
        response    = gpt_turn.get("value", "").strip()
        if not instruction or not response:
            continue
        prompt = ALPACA_WITH_INPUT.format(instruction=instruction, input=set_instructions)
        out.append({"prompt": prompt, "response": response})
    return out


def load_everyday_conversations(cache_dir: str) -> list[dict]:
    """
    HuggingFaceTB/everyday-conversations-llama3.1-2k
    Short, natural everyday dialogues (~2 k conversations) generated by
    Llama-3.1.  Good for teaching casual conversational tone.

    """
    ds = load_dataset(
        "HuggingFaceTB/everyday-conversations-llama3.1-2k",
        split="train_sft",
        cache_dir=cache_dir,
    )
    out = []
    for ex in ds:
        messages = ex.get("messages", [])
        if not messages:
            continue

        history = ""
        #set_instructions = f"You are a helpful AI assistant who gives a brief, clear and concise answers."
        set_instructions = f"You are an AI assistant who provides simple and easy to understand answers to casual user queries"
        idx = 2  # first pair of conversatoin is always user-> "Hi"/"Hey" assistant-> "Hello, How can I help you?" so not useful
        if len(messages) <= 1:
            continue
        else:
            if messages[idx].get("role") == "user":
                prompt = ALPACA_WITH_INPUT.format(instruction=messages[idx].get("content", ""), input=set_instructions)
                response = messages[idx+1].get("content", "")
            elif messages[idx].get("role") == "assistant":
                prompt = ALPACA_WITH_INPUT.format(instruction=messages[idx+1].get("content", ""), input=set_instructions)
                response = messages[idx+2].get("content", "")
            else:
                continue
            out.append({"prompt": prompt, "response": response})
    return out


