"""
SFT Data Preparation Pipeline

Label-masking strategy
-----------------------
Input tokens  (system prompt + user turn)  → label = IGNORE_INDEX (-100)
Output tokens (assistant response)         → label = token id
Loss is computed ONLY on response tokens, never on the prompt.

  train_tokens.bin   - uint32 token ids,  shape (N_train,)
  train_labels.bin   - int32  label ids,  shape (N_train,)   -100 = masked
  val_tokens.bin     - uint32 token ids,  shape (N_val,)
  val_labels.bin     - int32  label ids,  shape (N_val,)
  val_examples.jsonl - human-readable held-out examples for inference testing
                     - one JSON object per line:
                     -   {"prompt": str, "response": str, "source": str}
  metadata.json      - statistics + config snapshot
"""

import os
import json
import random
import argparse
import re
import numpy as np
import tiktoken
from tqdm import tqdm
from datasets import load_dataset

# constants
IGNORE_INDEX = -100          # positions masked from loss
EOT_TOKEN    = None          # resolved at runtime from tiktoken enc.eot_token
END_TOKEN_STR = "\n### END"

#Alpaca-style prompt templates
'''
ALPACA_WITH_INPUT = (
    "### Instruction:\n{instruction}\n\n"
    "### Input:\n{input}\n\n"
    "### Response:\n"
)
'''

ALPACA_WITH_INPUT = (
    "### SYSTEM:\n{input}\n\n"
    "### Instruction:\n{instruction}\n\n"
    "### Response:\n"
)

ALPACA_NO_INPUT = (
    "### Instruction:\n{instruction}\n\n"
    "### Response:\n"
)


def load_alpaca(cache_dir: str) -> list[dict]:
    """tatsu-lab/alpaca  –  52 k single-turn instruction examples."""
    ds = load_dataset("tatsu-lab/alpaca", split="train", cache_dir=cache_dir)
    out = []
    for ex in ds:
        instruction = ex.get("instruction", "").strip()
        inp         = ex.get("input", "").strip()
        response    = ex.get("output", "").strip()
        if not instruction or not response:
            continue
        prompt = ALPACA_WITH_INPUT.format(instruction=instruction, input=inp) \
                 if inp else ALPACA_NO_INPUT.format(instruction=instruction)
        out.append({"prompt": prompt, "response": response})
    return out


