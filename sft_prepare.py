def load_smollm_basics(cache_dir: str) -> list[dict]:
    """
    HuggingFaceTB/instruct-data-basics-smollm-H4
    
    """
    ds = load_dataset(
        "HuggingFaceTB/instruct-data-basics-smollm-H4",
        split="train_sft",
        cache_dir=cache_dir,
    )
    p_hf    = re.compile(r"Hugging\s*Face", re.IGNORECASE)
    p_smol  = re.compile(r"SmolLM",         re.IGNORECASE)
    set_instructions = f"You are a friendly assistant who responds with a casual yet short and simple greet. If instructed, introduce yourself and respond about your identity"
    out = []
    for ex in ds:
        messages = ex.get("messages", [])
        # Find the first user turn and the assistant turn immediately after it
        if not messages:
            continue
        user_content = ex.get("instruction", "").strip()
        asst_content = ex.get("response", "").strip()
        if asst_content == "":
            continue
        user_content = p_smol.sub("customLM",  p_hf.sub("unknownuser", user_content))
        asst_content = p_smol.sub("customLM",  p_hf.sub("unknownuser", asst_content))
        
        prompt = (
            ALPACA_WITH_INPUT.format(instruction=user_content, input=set_instructions)
        )
        out.append({
            "prompt": prompt,
            "response": asst_content,
        })
    return out

def load_smoltalk_filtered(cache_dir: str) -> list[dict]:
    """
    enPurified/smoltalk-creative-writing-enPurified-openai-messages — ~10k filtered conversations from SmolLM's training data.
    """
    ds = load_dataset("enPurified/smoltalk-creative-writing-enPurified-openai-messages", split="train", cache_dir=cache_dir)
    set_instructions = f"You are an instruction-following creative AI Assistant. Below is an instruction that describes a task. Write a response that appropriately completes the request"
    out = []
    for ex in ds:
        messages = ex.get("messages", [])
        prompt = ""
        asst_content = ""
        user_content = ""
        if len(messages) < 2:
            continue
        if messages[0]["role"] == "user" and messages[1]["role"] == "assistant":
            user_content = messages[0]["content"]
            asst_content = messages[1]["content"]
            prompt = ALPACA_WITH_INPUT.format(instruction=user_content, input=set_instructions) # no input form
        elif messages[0]["role"] == "system":
            user_content = messages[1]["content"]
            asst_content = messages[2]["content"]
            user_content = messages[0]["content"] + "\n" + user_content # system + user content
            prompt = ALPACA_WITH_INPUT.format(instruction=user_content, input=set_instructions) # with input form
        else:
            continue
        out.append({
            "prompt": prompt,
            "response": asst_content,
        })
    return out

